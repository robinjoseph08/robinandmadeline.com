// ListPartiesQuery is the set of party list filters plus the sort, bound from the
// query string by the custom binder (gorilla/schema via the `query` tag). A nil
// filter pointer means "do not filter on this field". Every filter except
// InfoCollectionStatus is applied in SQL; status is computed in Go (it depends on
// the derived rules) and filtered after the query. Sort is a multi-level sort
// spec ("side:asc,name:asc"), parsed and applied via pkg/sortspec; an empty Sort
// keeps the default (creation order). Enum filters and Sort are validated
// (omitempty so an absent value is fine); a bad value is a 422 (the sortspec
// validator checks Sort against the party field whitelist).
type ListPartiesQuery struct {
	Side                    *string `query:"side" json:"side" validate:"omitempty,oneof=robin madeline"`
	Relation                *string `query:"relation" json:"relation" validate:"omitempty,oneof=family friend"`
	Circle                  *string `query:"circle" json:"circle" validate:"omitempty,oneof=Immediate Extended College Work Childhood Other"` // matches parties whose circle array contains this value
	InvitationType          *string `query:"invitation_type" json:"invitation_type" validate:"omitempty,oneof=physical digital"`
	InfoCollectionRequested *bool   `query:"info_collection_requested" json:"info_collection_requested"`
	InfoCollectionStatus    *string `query:"info_collection_status" json:"info_collection_status" validate:"omitempty,oneof=complete incomplete" tstype:"models.InfoCollectionStatus"`
	Sort                    string  `query:"sort" json:"sort,omitempty" validate:"omitempty,max=200,sortspec=parties"`
}

// CreateGuestPayload is the body to add a guest to a party. The party comes from
// the route (create is nested under the party), so it is not a field here.
// is_primary may be requested; when true the service demotes any existing
// primary in the same transaction. Validation is tag-driven through the custom
// binder. tags is open-ended (no closed union) so it only bounds element
// length; it defaults to [] so a nil slice stores '{}', not NULL.
//
// placeholder_text marks the guest as an unnamed plus-one slot and stores its
// permanent descriptor; like rsvp_code it uses min=1 so a present-but-blank
// value is a 422 (a guest either has a descriptor or arrives as JSON null, a
// regular guest).
type CreateGuestPayload struct {
	FullName   string   `json:"full_name" mod:"trim" validate:"required,max=200"`
	Email      *string  `json:"email" mod:"trim" validate:"omitempty,email,max=320"`
	Phone      *string  `json:"phone" mod:"trim,phone" validate:"omitempty,phone,max=32"`
	Tags       []string `json:"tags" mod:"dive,trim" validate:"omitempty,dive,min=1,max=100" default:"[]"`
	IsPrimary  bool     `json:"is_primary"`
	IsChild    bool     `json:"is_child"`
	IsDrinking bool     `json:"is_drinking"`
	// Subscribed is the email opt-in (ADR 0009). A pointer so an omitted value
	// defaults to subscribed in the service: a new guest is born subscribed
	// unless the create form unchecks it (its Go zero value is false, the wrong
	// default).
	Subscribed          *bool   `json:"subscribed"`
	PlaceholderText     *string `json:"placeholder_text" mod:"trim" validate:"omitempty,min=1,max=200"`
	DietaryRestrictions *string `json:"dietary_restrictions" mod:"trim" validate:"omitempty,max=1000"`
	TableNumber         *int    `json:"table_number" validate:"omitempty,min=1"`
	SeatNumber          *int    `json:"seat_number" validate:"omitempty,min=1"`
}

