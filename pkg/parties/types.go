// PatchGuestPayload is a partial update of a guest's editable fields, the guest
// analogue of PatchPartyPayload: a nil pointer (an absent JSON key) leaves that
// column unchanged, while a provided field is applied. It backs the
// spreadsheet's single-cell guest edits (PATCH); the PUT full-state payload
// (UpdateGuestPayload) backs the dialog "full edit". Setting is_primary=true
// promotes this guest and demotes the party's previous primary transactionally.
//
// Validation is uniformly omitempty: an absent field is skipped, full_name keeps
// min=1 so blanking the name cell is a 422, and email uses emailblank so a
// provided blank clears it (the service stores NULL) while a present value is
// still format-checked. placeholder_text permits blank (max-only) because a
// provided blank is the grid's "clear this cell" gesture, stored as SQL NULL,
// which turns the row back into a regular guest. tags is a plain slice: nil
// leaves it unchanged, a present array (including []) replaces it. party_id
// moves the guest to another party (the flat guest list edits it inline); the
// service checks the target party exists and keeps the single-primary
// invariant in the destination.
type PatchGuestPayload struct {
	PartyID             *string  `json:"party_id,omitempty" validate:"omitempty,uuid"`
	FullName            *string  `json:"full_name,omitempty" mod:"trim" validate:"omitempty,min=1,max=200"`
	Email               *string  `json:"email,omitempty" mod:"trim" validate:"omitempty,emailblank,max=320"`
	Phone               *string  `json:"phone,omitempty" mod:"trim,phone" validate:"omitempty,phone,max=32"`
	Tags                []string `json:"tags,omitempty" mod:"dive,trim" validate:"omitempty,dive,min=1,max=100"`
	IsPrimary           *bool    `json:"is_primary,omitempty"`
	IsChild             *bool    `json:"is_child,omitempty"`
	IsDrinking          *bool    `json:"is_drinking,omitempty"`
	PlaceholderText     *string  `json:"placeholder_text,omitempty" mod:"trim" validate:"omitempty,max=200"`
	DietaryRestrictions *string  `json:"dietary_restrictions,omitempty" mod:"trim" validate:"omitempty,max=1000"`
	TableNumber         *int     `json:"table_number,omitempty" validate:"omitempty,min=1"`
	SeatNumber          *int     `json:"seat_number,omitempty" validate:"omitempty,min=1"`
}

// ListGuestsQuery is the set of flat guest-list filters plus the sort, bound from
// the query string by the custom binder (gorilla/schema via the `query` tag).
// Side / Relation / Circle are party-level attributes, so those clauses join
// through the guest's party. EventID / RSVPStatus filter through the guest's
// Event RSVP rows (a row is the invitation, ADR 0002): an event alone matches
// that event's invited set, adding a status constrains within that event, and a
// status alone matches guests holding a row in that status on any event.
type ListGuestsQuery struct {
	Search   *string `query:"search" json:"search"`                               // case-insensitive match on name, email, phone, or party name
	PartyID  *string `query:"party_id" json:"party_id" validate:"omitempty,uuid"` // matches guests in this one party
	Side     *string `query:"side" json:"side" validate:"omitempty,oneof=robin madeline"`
	Relation *string `query:"relation" json:"relation" validate:"omitempty,oneof=family friend"`
	Circle   *string `query:"circle" json:"circle" validate:"omitempty,oneof=Immediate Extended College Work Childhood Other"`
	// Tags is intentionally unvalidated: tags are an open set (no closed
	// union), so any value is a legal filter that simply may match nothing.
	// Multiple tags are OR'd (a guest matches when its tags include ANY of
	// them); the repeated ?tags=a&tags=b query form binds via the query alias,
	// and omitempty keeps an absent filter from constraining.
	Tags       []string `query:"tags,omitempty" json:"tags,omitempty"`
	IsDrinking *bool    `query:"is_drinking" json:"is_drinking"`
	IsChild    *bool    `query:"is_child" json:"is_child"`
	// IsPlaceholder filters on the derived placeholder condition (true matches
	// guests whose placeholder_text is set, false those where it is NULL);
	// there is no stored boolean.
	IsPlaceholder *bool   `query:"is_placeholder" json:"is_placeholder"`
	EventID       *string `query:"event_id" json:"event_id" validate:"omitempty,uuid"` // matches guests invited to this event
	RSVPStatus    *string `query:"rsvp_status" json:"rsvp_status" validate:"omitempty,oneof=pending attending not_attending" tstype:"models.EventRSVPStatus"`
	// Sort is a multi-level sort spec ("party:asc,name:asc"); an empty Sort keeps
	// the default (creation order). The "party" field sorts a guest by its owning
	// party's name. Validated against the guest field whitelist by the sortspec
	// validator (a bad value is a 422).
	Sort string `query:"sort" json:"sort,omitempty" validate:"omitempty,max=200,sortspec=guests"`
}

// PartyResponse is the API representation of a party: the stored model plus the
// derived info_collection_status. The model is embedded by value (not as a
// pointer) so tygo flattens it into a plain `extends models.Party`; a pointer
// embed would generate `extends Partial<models.Party>`, making every model field
// optional on the client and defeating the point of the generated types. The
// embedded models.Party already carries the snake_case JSON tags.
type PartyResponse struct {
	models.Party         `tstype:",extends"`
	InfoCollectionStatus string `json:"info_collection_status" tstype:"models.InfoCollectionStatus"`
	// MissingRequiredFields itemizes the required fields the party still lacks
	// (the human-readable labels from models.Party.MissingRequiredFields), so
	// the admin UI can explain an incomplete status on hover. Empty (never
	// null) when nothing is missing.
	MissingRequiredFields []string `json:"missing_required_fields"`
}

// ListPartiesResponse is the uniform list envelope for parties.
type ListPartiesResponse struct {
	Items []PartyResponse `json:"items"`
	Total int             `json:"total"`
}

// GuestResponse is the API representation of a single guest, used by the
// party-scoped create/update endpoints. No reshape is needed, so it is a thin
// embed of the stored model (by value, so tygo emits a plain `extends
// models.Guest` rather than a Partial; see PartyResponse).
type GuestResponse struct {
	models.Guest `tstype:",extends"`
}

// GuestListItem is the API representation of a guest in the flat guest list. A
// guest is a sub-entity of its party and has no detail page of its own, so the
// list carries the owning party's name (alongside the model's party_id) to let
// the UI link each guest back to its party and edit it in place. It embeds the
// guest model by value (a plain `extends models.Guest`; see PartyResponse) and
// adds only the derived party_name.
type GuestListItem struct {
	models.Guest `tstype:",extends"`
	PartyName    string `json:"party_name"`
}

// ListGuestsResponse is the uniform list envelope for the flat guest list. Items
// are GuestListItem (guest plus party_name), not GuestResponse, because the flat
// list needs the party context the party-scoped endpoints already have.
type ListGuestsResponse struct {
	Items []GuestListItem `json:"items"`
	Total int             `json:"total"`
}

// newPartyResponse wraps a loaded party (with guests) for the API, computing
// its status and the itemized missing required fields. Guests must be loaded
// for both to be accurate. The derived values are computed before the model is
// copied into the response by value.
func newPartyResponse(p *models.Party) PartyResponse {
	return PartyResponse{
		Party:                 *p,
		InfoCollectionStatus:  p.InfoCollectionStatus(),
		MissingRequiredFields: p.MissingRequiredFields(),
	}
}

// newGuestResponse wraps a guest for the API.
func newGuestResponse(g *models.Guest) GuestResponse {
	return GuestResponse{Guest: *g}
}

// newGuestListItem wraps a guest for the flat guest list, carrying the owning
// party's name. The guest's Party relation must be loaded; a guest with no
// loaded party falls back to an empty name rather than panicking.
func newGuestListItem(g *models.Guest) GuestListItem {
	item := GuestListItem{Guest: *g}
	if g.Party != nil {
		item.PartyName = g.Party.Name
	}
	return item
}
